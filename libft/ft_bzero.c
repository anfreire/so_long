#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*c;

	i = 0;
	c = s;
	if (n == 0)
		return ;
	while (i < n)
	{
		c[i] = 0;
		i++;
	}
}
